#include "libft.h"

void	*ft_memset(void *obj, int ch, size_t num)
{
	char	*buf;
	size_t	x;

	buf = obj;
	x = 0;
	while (++x <= num)
		buf[x - 1] = ch;
	return ((void *)obj);
}
