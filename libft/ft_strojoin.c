#include "libft.h"

int	ft_strojoin(char **s1, char const *s2)
{
	char	*ret;

	if (!(ret = ft_strjoin(*s1, s2)))
		return (0);
	ft_strdel(s1);
	*s1 = ret;
	return (1);
}
